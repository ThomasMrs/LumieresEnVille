COULEUR_PAR_DEFAUT = (0, 255, 255) 



ALPHABET_POLAIRE = {}

for lettre, lignes in LETTRES_BRUTES.items():
    matrice_polaire = {}
    
    couleur_rgb = COULEURS_LETTRES.get(lettre, COULEUR_PAR_DEFAUT)
    
    for y, ligne in enumerate(lignes):
        rayon = y + 3 
        
        for x, bit in enumerate(ligne):
            if bit == "1":
                angle = 180 + (x * 10) 
                
                if angle not in matrice_polaire:
                    matrice_polaire[angle] = []
                    
                matrice_polaire[angle].append((rayon, couleur_rgb))
                
    ALPHABET_POLAIRE[lettre] = matrice_polaire